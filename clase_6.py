# Operadores numéricos
a = 10
b = 3

print('SUMA:', a + b)
print('RESTA:', a - b)
print('DIVISIÓN:', a / b)
print('MULTIPLICACIÓN:', a * b)

print('MÓDULO:', a % b)
print('PARTE ENTERA DE LA DIVISIÓN:', a // b)
print('POTENCIACIÓN:', a ** b)

a += 2
print(a)

